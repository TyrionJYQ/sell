<template>
  <div>
    <div class="goods">
      <div class="menu-wrapper" ref="menuWrapper">
        <ul>
          <li v-for="(item,index) in goods" class="menu-item" :class="{'current':currentIndex === index}"  @click = "selectMenu(index,$event)" ref="menuList">
            <span class="text border-1px">
              <span v-if="item.type>0" :class="classMap[item.type]" class="icon"></span>{{item.name}}
            </span>
          </li>  
        </ul>      
      </div>
      <div class="foods-wrapper" ref="foodsWrapper">
          <ul>
              <li v-for="(item,index) in goods"  class="food-list" ref="foodList">
                  <h1 class="title">{{item.name}}</h1>
                  <ul>
                      <li v-for="(food,index) in item.foods" @click="selectFood(food,$event)" class="food-item border-1px">
                          <div class="icon">
                              <img width="57" height="57" :src="food.icon" alt="">
                          </div>
                          <div class="content">
                              <h2 class="name">{{food.name}}</h2>
                              <p class="desc">{{food.description}}</p>
                              <div class="extra">
                                  <span class="count">月售{{food.sellCount}}份</span><span class="">好评率{{food.rating}}%</span>
                              </div>
                              <div class="price">
                                  <span class="now">￥{{food.price}}</span><span v-if="food.oldPrice" class="old">￥{{food.oldPrice}}</span>
                              </div>
                              <div class="cartcontrol-wrapper">
                                <cartcontrol :food="food" @add="addFood"></cartcontrol>
                              </div>
                          </div>
                      </li>
                  </ul>
              </li>
          </ul>
      </div>
      <shopcart ref="shopcart" :delivery-price="seller.deliveryPrice" :min-price="seller.minPrice" :select-foods="selectFoods"></shopcart> 
    </div>
    <food :food="selectedFood" ref="food" @add="addFood"></food>
  </div>    
</template>

<script type='text/ecmascript-6'>
import BScroll from "better-scroll";
import shopcart from "components/shopcart/shopcart.vue"
import cartcontrol from "components/cartcontrol/cartcontrol.vue"
import food from "components/food/food.vue"
const ERR_OK = 0;
const debug = process.env.NODE_ENV !== 'production';
export default {
  props: {
    seller: {
      type: Object
    }
  },
  data() {
    return {
      goods: [],
      listHeight: [],
      scrollY: 0,
      selectedFood: {}
    };
  },
  computed: {
      // currentIndex () {
      //     for(let i = 0;i < this.listHeight.length; i++) {
      //         let height1 = this.listHeight[i];
      //         console.log('height1:' + height1);
      //         let height2 = this.listHeight[i+1];
      //         console.log('scrollY:'+this.scrollY);
      //         if(!height2 || (this.scrollY >= height1 && this.scrollY <= height2)) {
      //             console.log('cnm');
      //             this._followScroll(i);
      //             console.log('done');
      //             return i;
                  
      //         }
              
      //     }
          
      //     return 0;
      // }
      currentIndex() {
        for (let i = 0; i < this.listHeight.length; i++) {
          let height1 = this.listHeight[i];
          let height2 = this.listHeight[i + 1];
          // console.log('scrollY:'+this.scrollY);
          
          if (!height2 || (this.scrollY >= height1 && this.scrollY < height2)) {
            this._followScroll(i);
            return i;
          }
        }
        return 0;
      },
      //选择的foods
      selectFoods () {
        let foods = [];
        this.goods.forEach( (good) => {
          good.foods.forEach( (food) => {
            if (food.count) {
              foods.push(food);
            }
          });
        });
        return foods;
        // console.log("foods:"+foods)
      }
      
  },
  created() {
    this.classMap = ['decrease','discount','guarantee','invoice','special'];
    this.$http.get("/api/goods").then(response => {
      response = response.body;
      //   console.log(response);
      if (response.errno === ERR_OK) {
        this.goods = response.data;
        // console.log(this.goods);
        this.$nextTick(() => {
            this._initScroll();
            this._calculateHeight();
        });
        
      }
    });
  },
  methods: {
      //初始化BScroll
      _initScroll() {
          this.menuScroll = new BScroll(
          this.$refs.menuWrapper,{
            click:true
          });
          this.foodsScroll = new BScroll(
          this.$refs.foodsWrapper,{
            click:true,
            probeType: 3
          });
          // this.foodsScroll.on('scroll', function(pos) {
          //   //pos是滚动的位置
          //   if(pos.y <= 0) {
          //     this.scrollY = Math.abs(Math.round(pos.y));
          //   }
          //   console.log(this.scrollY);
          // });
          /*箭头函数this能同步更新scrollY*/ 
          this.foodsScroll.on('scroll', (pos) => {
            if (pos.y <= 0) {
              this.scrollY = Math.abs(Math.round(pos.y));
            }
          });
          
      },
      //计算高度
      _calculateHeight() {
        let foodList = this.$refs.foodList;
        // console.log(foodList);
        let height = 0;
        this.listHeight.push(height);
        for(let i = 0; i < foodList.length; i++) {
            let item = foodList[i];
            height += item.clientHeight;
            this.listHeight.push(height);
        // console.log('listHeight1'+":"+this.listHeight);    
        }
        // console.log('listHeight'+":"+this.listHeight);
      },
      _followScroll(index) {
          let menuList = this.$refs.menuList;
          // console.log("menuList=" + menuList);
          let el = menuList[index];
          this.menuScroll.scrollToElement(el,300,0,-100);

      },
      selectMenu (index,event) {
        if(!event._constructed) {
          return;
        }
        // console.log(index);
        let foodList = this.$refs.foodList;
        let el = foodList[index];
        this.foodsScroll.scrollToElement(el, 300);
      },
      addFood (target) {
        this._drop(target);
      },
      _drop(target) {
        //体验优化，异步执行下落动画
        this.$nextTick(this.$refs.shopcart.drop(target));
        
      },
      //selectFood
      selectFood(food,event) {
        // if(event._constructed) {
        //   return;
        // }
        this.selectedFood = food;
        console.log(food);
        this.$refs.food.show();
      }
  },
  components : {
    shopcart,
    cartcontrol,
    food
  }
};
</script>

